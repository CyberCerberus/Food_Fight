package gameCore;

public class LevelUpBuild {
	private int str, def, spd;
	
	public LevelUpBuild(int str, int def, int spd) {
		this.str = str;
		this.def = def;
		this.spd = spd;
	}
	
	public int getStr() {
		return str;
	}
	public int getDef() {
		return def;
	}
	public int gedSpd() {
		return spd;
	}
}
